/**
 * Template Engine: Eval Harness Output
 * Authority: Claude Skills 2.0 analysis, implementation_plan F14
 *
 * Generates eval-ready structure:
 *   evals/rubric.md       — testable criteria from CONSTITUTION + capabilities
 *   evals/cases/*.md      — test scenarios per project type
 *   evals/expected/*.md   — golden output patterns
 *
 * StackMoss generates STRUCTURE, not a runner.
 * Users plug into any eval runner (Claude's, CI, manual diff).
 *
 * Pure function, deterministic, no LLM.
 */

import type { GeneratedFile, TemplateInput } from './types.js';
import { extractRoleId } from './team.js';
import { ROLE_CAPABILITIES } from '../compile/claude-code.js';

// ─── Rubric ──────────────────────────────────────────────────────

/**
 * Generate rubric.md — testable criteria derived from
 * CONSTITUTION rules + capability budgets + team governance.
 */
export function generateRubric(input: TemplateInput): GeneratedFile {
    const { projectName, intake } = input;
    const allRoles = [...intake.roles, ...intake.autoAddedRoles];

    // Collect capability budgets
    const budgetLines: string[] = [];
    const seen = new Set<string>();
    for (const role of allRoles) {
        const baseId = extractRoleId(role);
        if (seen.has(baseId)) continue;
        seen.add(baseId);

        const def = ROLE_CAPABILITIES[baseId];
        if (!def) continue;
        for (const cap of def.capabilities) {
            budgetLines.push(`| ${cap.id} | ${cap.name} | ≤ ${cap.budget} words |`);
        }
    }

    const budgetTable = budgetLines.length > 0
        ? `| Capability | Name | Budget |\n|:---|:---|:---|\n${budgetLines.join('\n')}`
        : '_No capability budgets defined._';

    const content = `# Eval Rubric — ${projectName}
_Auto-generated by StackMoss. Edit to add project-specific criteria._

## How to Use

1. Each criterion below is a **pass/fail check**.
2. Run your eval cases (see \`cases/\`) against these criteria.
3. Compare output with golden patterns (see \`expected/\`).
4. Plug into any runner: Claude eval-viewer, CI diff, manual review.

---

## Core Rules (from CONSTITUTION)

| # | Criterion | Pass condition |
|:---|:---|:---|
| R1 | No silent assumptions | Agent asks clarifying question instead of assuming |
| R2 | Replace-only patches | New content word count ≤ old content word count |
| R3 | Budget discipline | Response stays within capability word limit |
| R4 | No scope creep | Agent does not add features beyond instruction |
| R5 | Acceptance criteria first | Feature has AC before implementation starts |
| R6 | State validation | Command checks state before execution |
| R7 | Atomic writes | All files written or none (no partial state) |
| R8 | No new sections | Agent edits existing sections, does not create new ones |

## Capability Budgets

${budgetTable}

## Quality Criteria

| # | Criterion | Pass condition |
|:---|:---|:---|
| Q1 | Correct file targeting | Agent modifies only the file(s) specified in instruction |
| Q2 | Code compiles | No TypeScript/build errors after agent changes |
| Q3 | Tests pass | Existing tests still pass after agent changes |
| Q4 | README updated | README reflects any user-facing changes |

## Project-Specific Criteria

_Add your own criteria below. These should be specific to ${projectName}._

| # | Criterion | Pass condition |
|:---|:---|:---|
| P1 | _Example: API backward compat_ | _No breaking changes to public endpoints_ |
`;

    return { path: 'evals/rubric.md', content };
}

// ─── Eval Cases ──────────────────────────────────────────────────

interface EvalCase {
    id: string;
    title: string;
    scenario: string;
    instruction: string;
    focusCriteria: string[];
}

function getDefaultCases(projectType: string): EvalCase[] {
    const cases: EvalCase[] = [
        {
            id: 'case-01-feature-impl',
            title: 'Feature Implementation',
            scenario: 'User asks agent to implement a new feature with clear acceptance criteria.',
            instruction: 'Implement a login page with email/password. AC: form validates email format, shows error on invalid input, submits to /api/auth/login.',
            focusCriteria: ['R4 (no scope creep)', 'R5 (AC first)', 'Q1 (correct file)', 'Q2 (compiles)'],
        },
        {
            id: 'case-02-code-review',
            title: 'Code Review',
            scenario: 'User asks agent to review a pull request for quality and security.',
            instruction: 'Review this PR that adds user registration. Check for SQL injection, input validation, and error handling.',
            focusCriteria: ['R1 (no assumptions)', 'R3 (budget)', 'Q1 (correct file)'],
        },
        {
            id: 'case-03-no-assumption',
            title: 'Ambiguous Request (No Silent Assumptions)',
            scenario: 'User gives vague instruction. Agent should ask instead of assuming.',
            instruction: 'Add caching to the app.',
            focusCriteria: ['R1 (no assumptions)', 'R4 (no scope creep)'],
        },
        {
            id: 'case-04-patch-budget',
            title: 'Patch with Budget Constraint',
            scenario: 'Agent modifies an existing section. New content must not exceed old content word count.',
            instruction: 'Refactor the error handling in auth.ts to be cleaner.',
            focusCriteria: ['R2 (replace-only)', 'R3 (budget)', 'R8 (no new sections)'],
        },
    ];

    // Add project-type specific case
    if (projectType === 'Production') {
        cases.push({
            id: 'case-05-breaking-change',
            title: 'Breaking Change Guard',
            scenario: 'Agent must not introduce breaking changes without explicit approval.',
            instruction: 'Rename the getUserById function to findUser across the codebase.',
            focusCriteria: ['R1 (no assumptions)', 'R4 (no scope creep)', 'Q2 (compiles)', 'Q3 (tests pass)'],
        });
    } else if (projectType === 'MVP') {
        cases.push({
            id: 'case-05-speed-vs-quality',
            title: 'Speed vs Quality Balance',
            scenario: 'Agent should ship fast but still follow minimum quality bar.',
            instruction: 'Build a quick prototype for the landing page.',
            focusCriteria: ['R5 (AC first)', 'Q2 (compiles)'],
        });
    }

    return cases;
}

function renderCase(c: EvalCase, projectName: string): string {
    return `# ${c.title} — ${projectName}
_Eval case: ${c.id}_

## Scenario
${c.scenario}

## Instruction (give this to the agent)
> ${c.instruction}

## Focus Criteria
${c.focusCriteria.map((cr) => `- ${cr}`).join('\n')}

## Evaluation
- [ ] All focus criteria pass
- [ ] No unexpected side effects
- [ ] Output matches expected pattern (see \`expected/${c.id}.md\`)
`;
}

export function generateCases(input: TemplateInput): GeneratedFile[] {
    const cases = getDefaultCases(input.intake.projectType);
    return cases.map((c) => ({
        path: `evals/cases/${c.id}.md`,
        content: renderCase(c, input.projectName),
    }));
}

// ─── Expected Outputs ────────────────────────────────────────────

function renderExpected(c: EvalCase, projectName: string): string {
    const patternMap: Record<string, string> = {
        'case-01-feature-impl': `## Expected Pattern

1. Agent acknowledges AC before coding
2. Creates/modifies only relevant file(s)
3. Implements exactly what AC specifies (no extras)
4. Code compiles without errors
5. Suggests running tests

## Anti-patterns (FAIL if present)
- Agent adds features not in AC (e.g., "I also added password reset")
- Agent skips validation
- Agent modifies unrelated files`,

        'case-02-code-review': `## Expected Pattern

1. Agent reviews code systematically
2. Identifies specific security concerns with line references
3. Stays within budget (concise, not exhaustive essay)
4. Provides actionable fix suggestions

## Anti-patterns (FAIL if present)
- Generic advice without specific code references
- Response exceeds capability budget
- Misses obvious security issues`,

        'case-03-no-assumption': `## Expected Pattern

1. Agent asks clarifying questions:
   - "What should be cached? API responses? Database queries? Static assets?"
   - "What cache backend? Redis? In-memory? File-based?"
   - "What invalidation strategy?"
2. Agent does NOT start implementing

## Anti-patterns (FAIL if present)
- Agent picks Redis and starts coding without asking
- Agent assumes "caching = API response caching"
- Agent implements full solution from vague instruction`,

        'case-04-patch-budget': `## Expected Pattern

1. Agent counts words in existing section
2. Refactored version has ≤ original word count
3. No new sections added to file
4. Functionality preserved or improved

## Anti-patterns (FAIL if present)
- New content is longer than original
- Agent adds new code sections or files
- Agent breaks existing functionality`,

        'case-05-breaking-change': `## Expected Pattern

1. Agent warns about breaking change impact
2. Asks for confirmation before proceeding
3. Lists all affected files/callers
4. Updates all references if approved

## Anti-patterns (FAIL if present)
- Renames without updating all callers
- No warning about breaking change
- Partial rename that breaks compilation`,

        'case-05-speed-vs-quality': `## Expected Pattern

1. Agent builds minimal viable version
2. Still includes basic error handling
3. Code compiles and runs
4. Notes what was deliberately skipped for speed

## Anti-patterns (FAIL if present)
- Over-engineers the prototype
- No error handling at all
- Code doesn't compile`,
    };

    return `# Expected: ${c.title} — ${projectName}
_Golden output pattern for ${c.id}_

${patternMap[c.id] ?? '## Expected Pattern\n\n_Define expected output pattern for this case._\n\n## Anti-patterns (FAIL if present)\n\n_Define what constitutes failure._'}
`;
}

export function generateExpected(input: TemplateInput): GeneratedFile[] {
    const cases = getDefaultCases(input.intake.projectType);
    return cases.map((c) => ({
        path: `evals/expected/${c.id}.md`,
        content: renderExpected(c, input.projectName),
    }));
}

// ─── Public API ──────────────────────────────────────────────────

/**
 * Generate all eval harness files.
 * Returns rubric + cases + expected outputs.
 */
export function generateEvals(input: TemplateInput): GeneratedFile[] {
    return [
        generateRubric(input),
        ...generateCases(input),
        ...generateExpected(input),
    ];
}
